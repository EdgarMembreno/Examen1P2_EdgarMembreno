    private void agregarLibroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_agregarLibroMouseClicked
        try {
            String titulo, decri, autor;
            String genero = "";
            int puntaje, copias, edicion, publi;
            int valor;

            titulo = Titulos.getText();

            decri = descripcion.getText();

            autor = Autor.getText();

            if (fantasia.isSelected()) {
                genero = "Fantasia";
            } else if (romance.isSelected()) {
                genero = "Romance";
            } else if (accion.isSelected()) {
                genero = "Accion";
            } else if (historia.isSelected()) {
                genero = "Historia";
            }

            puntaje = Integer.parseInt(puntuaje.getText());
            copias = Integer.parseInt(this.copias.getText());
            edicion = Integer.parseInt(this.edicion.getText());
            publi = Integer.parseInt(publicacion.getText());
            valor = Integer.parseInt(Valor.getText());

            Libros l = new Libros(titulo, decri, puntaje, copias, genero, valor, edicion, autor, valor);
            libros.add(new Libros(titulo, decri, puntaje, copias, genero, valor, edicion, autor, publi));

            Titulos.setText("");
            descripcion.setText("");
            puntuaje.setText("");
            this.copias.setText("");
            fantasia.setText("");
            Valor.setText("");
            this.edicion.setText("");
            Autor.setText("");
            Valor.setText("");
            publicacion.setText("");

            Object[] LibroNuevo = {l.getTitulo(), l.getGenero(), l.getAutor(), l.getDescripcion(), l.getPuntaje(), l.getCopiasDisponibles(), l.getEdicion(), l.getAño(), l.getValor()};
            DefaultTableModel t = (DefaultTableModel) EliminarLibro.getModel();
            t.addRow(LibroNuevo);
            DefaultTableModel m = (DefaultTableModel) ModificarLibro.getModel();
            m.addRow(LibroNuevo);

            Historial.append("El libro : " + titulo + " Fue agregado");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocurrio un error");
        }


    }//GEN-LAST:event_agregarLibroMouseClicked

