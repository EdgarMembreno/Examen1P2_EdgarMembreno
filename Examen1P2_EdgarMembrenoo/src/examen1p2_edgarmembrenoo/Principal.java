    private void Agregar2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Agregar2MouseClicked
        try {

            ArrayList<String> libr = new ArrayList<String>();
            ArrayList<String> amig = new ArrayList<String>();
            String usuario, correo, nombre;
            String libros, amigos;
            String gene;
            int numero, contra;
            Date naci;

            usuario = this.usuario.getText();
            contra = Integer.parseInt(Contraseña.getText());
            correo = Correo.getText();
            nombre = Nombre.getText();
            gene = GeneroFav.getText();
            numero = Integer.parseInt(Numero.getText());
            naci = nacimiento.getDate();

            libros = LibrosLista.getText();
            amigos = Amigos.getText();

            libr.add(libros);
            amig.add(amigos);

            Usuarios us = new Usuarios(usuario, contra, naci, numero, correo, nombre, gene, libr, amig);
            Usua.add(new Usuarios(usuario, contra, naci, numero, correo, nombre, gene, libr, amig));

            this.usuario.setText("");
            Contraseña.setText("");
            Correo.setText("");
            Nombre.setText("");
            GeneroFav.setText("");
            Numero.setText("");
            LibrosLista.setText("");
            Amigos.setText("");

            Object[] UsuaNuevo = {us.getNombreUsuario(), us.getContraseña(), us.getFecha(), us.getNumero(), us.getCorreo(), us.getNombre(), us.getGeneFvorito(), us.getLibros(), us.getAmigos()};
            DefaultTableModel u = (DefaultTableModel) eUsuario.getModel();
            u.addRow(UsuaNuevo);
            DefaultTableModel mu = (DefaultTableModel) mUsuario.getModel();
            mu.addRow(UsuaNuevo);

            Historial.append("El Usuario : " + usuario + " Fue agregado");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocurrio un error ");
        }


    }//GEN-LAST:event_Agregar2MouseClicked

